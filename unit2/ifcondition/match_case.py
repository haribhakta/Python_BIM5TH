# WAP in python to enter a number from 1-7 and print name of weekday
n=int(input("Enter a numnber from 1-7 = "))
match n:
    case 1: 
        print("Sunday")
    case 2:
        print("Monday")
    case 3:
        print("Tuesday")
    case 4:
        print("Wednesay")
    case 5:
        print("Thursday")
    case 6:
        print("Friday")
    case 7:
        print("Saturday")
    case _:
        print("Wrong Entry")
