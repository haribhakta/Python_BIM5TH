# Boolean operation
a = True
b = False
print("a and b = ",a and b)  # Output: False
print("a or b = ",a or b)   # Output: True
print("Not a = ",not a)    # Output: False

# convert into boolean
print("Convert 0 = ",bool(0))        # Output: False
print("Convert 1 = ",bool(1))        # Output: True
print("Convert empty collection [] = ",bool([]))       # Output: False
print("Convert collection () = ",bool([1, 2]))   # Output: True

